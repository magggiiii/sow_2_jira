import json
import os
from typing import List, Dict, Any, Optional
from pydantic import BaseModel, Field
from langchain_openai import ChatOpenAI
from langchain.prompts import ChatPromptTemplate
from models.eval_schemas import HierarchicalDatasetItem, GoldenEpic, GoldenTicket

# ─── Evaluator Schemas ────────────────────────────────────────────────────────

class EvaluationScores(BaseModel):
    alignment: float = Field(description="Epic level alignment (0-1)")
    recall: float = Field(description="Story level recall (0-1)")
    fidelity: float = Field(description="Story level fidelity/precision (0-1)")
    hallucination: float = Field(description="Presence of hallucinations (0: none, 1: high)")
    reasoning: str = Field(description="Detailed reasoning for the scores")

# ─── Hierarchical Judge ───────────────────────────────────────────────────────

class HierarchicalJudge:
    """LLM-as-a-judge for comparing EPIC -> Tickets hierarchies."""

    def __init__(self, model_name: str = "ollama/llama3", base_url: Optional[str] = None):
        # Default to Admin Bifrost if not provided
        self.base_url = base_url or os.getenv("BIFROST_URL", "http://s2j-admin-bifrost:8081/v1")
        self.api_key = os.getenv("BIFROST_API_KEY", "bifrost-admin-key")
        
        self.llm = ChatOpenAI(
            model=model_name,
            openai_api_base=self.base_url,
            openai_api_key=self.api_key,
            temperature=0
        )
        
        self.prompt_template = ChatPromptTemplate.from_messages([
            ("system", """You are an expert software engineer and QA analyst.
Your task is to evaluate the quality of a 'Jira Extraction' from a Statement of Work (SOW).
The extraction is hierarchical: EPIC -> Tickets (Stories).

Compare the **Actual Output** (generated by the AI) against the **Expected Output** (Ground Truth).

### Criteria:
1. **Alignment**: Does the generated Epic title and description match the intent of the Ground Truth Epic?
2. **Recall**: How many of the required Stories from the Ground Truth are present in the Actual Output? (Matches can be semantic).
3. **Fidelity**: For the stories that match, how accurate are the details (description, ACs)?
4. **Hallucination**: Does the Actual Output contain stories or requirements that are NOT in the Ground Truth or the SOW text?

### Output Format:
Return ONLY a valid JSON object with the following keys:
- alignment: (0.0 to 1.0)
- recall: (0.0 to 1.0)
- fidelity: (0.0 to 1.0)
- hallucination: (0.0 to 1.0)
- reasoning: (string explaining the scores)
"""),
            ("user", """### Source Text:
{source_text}

### Expected Output (Ground Truth):
{expected_output}

### Actual Output (Generated):
{actual_output}

Evaluate the extraction quality and return the JSON scores.""")
        ])

    def evaluate(self, source_text: str, expected_output: Dict[str, Any], actual_output: List[Dict[str, Any]]) -> EvaluationScores:
        """
        Evaluate a single dataset item comparison.
        """
        formatted_expected = json.dumps(expected_output, indent=2)
        formatted_actual = json.dumps(actual_output, indent=2)
        
        messages = self.prompt_template.format_messages(
            source_text=source_text,
            expected_output=formatted_expected,
            actual_output=formatted_actual
        )
        
        # Invoke LLM directly for easier mocking
        response = self.llm.invoke(messages)
        
        try:
            content = response.content
            if hasattr(content, "decode"): # Handle bytes if necessary
                content = content.decode("utf-8")
            
            # Clean JSON if necessary
            if "```json" in content:
                content = content.split("```json")[1].split("```")[0].strip()
            elif "```" in content:
                content = content.split("```")[1].split("```")[0].strip()
            
            data = json.loads(content)
            return EvaluationScores(**data)
        except Exception as e:
            return EvaluationScores(
                alignment=0, recall=0, fidelity=0, hallucination=1.0,
                reasoning=f"Error parsing judge response: {str(e)}\nRaw: {getattr(response, 'content', response)}"
            )
